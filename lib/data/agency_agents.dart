  /// Get agents by division
  static List<AgentPersonality> getByDivision(AgentDivision division) {
    return allAgents.where((agent) => agent.division == division).toList();
  }

  /// Get agent by ID
  static AgentPersonality? getById(String id) {
    try {
      return allAgents.firstWhere((agent) => agent.id == id);
    } catch (_) {
      return null;
    }
  }

  /// Search agents by name or specialty
  static List<AgentPersonality> search(String query) {
    final lowerQuery = query.toLowerCase();
    return allAgents.where((agent) {
      return agent.name.toLowerCase().contains(lowerQuery) ||
          agent.shortDescription.toLowerCase().contains(lowerQuery) ||
          agent.specialties.any((s) => s.toLowerCase().contains(lowerQuery));
    }).toList();
  }

  /// Get all divisions with agent counts
  static Map<AgentDivision, int> getDivisionCounts() {
    final counts = <AgentDivision, int>{};
    for (final division in AgentDivision.values) {
      counts[division] = getByDivision(division).length;
    }
    return counts;
  }

